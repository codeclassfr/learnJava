package fr.codecalss.tp.reponses._01basiques._02operateur;

public class AnswerTP3 {

    public static void main(String[] args) {

        // déclarer une variable type boolean, name 'oui', valeur true;
        // déclarer une variable type boolean, name 'non', valeur false;
        boolean oui = true;
        boolean non = false;



        // imprimer dans la console oui && non pour voir le résultat;
        System.out.println("oui && non :" + (oui && non));

        /* ensuite, essayer
            oui || non
         */
        System.out.println("oui || non :" + (oui || non));
    }
}
